
public class Bridge {
	
	private String name;
	private double weight;
	
	Bridge()
	{
		this.name = "";
		this.weight = 0.0;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public double getWeight() {
		return weight;
	}

	public void setWeight(double weight) {
		this.weight = weight;
	}
	
	

}
